function infoToggle() {
    var x = document.getElementById("homepage");
    if (x.style.display === "block") {
      x.style.display = "none";
    } else {
      x.style.display = "none";
    }
    var x = document.getElementById("infoTab");
    if (x.style.display === "none") {
        x.style.display = "block";
      } else {
        x.style.display = "none";
      }
      var x = document.getElementById("detailTab");
    if (x.style.display === "block") {
        x.style.display = "none";
      } else {
        x.style.display = "none";
      }
      var x = document.getElementById("guidelinesTab");
      if (x.style.display === "block") {
          x.style.display = "none";
        } else {
          x.style.display = "none";
        }
  }

